  ll n; cin >> n;
  // n / m == n % m == x
  // x * (m+1) == n
  vector<ll> div;
  find_divisor(n, div);
  ll ans = 0;
  for(int m: div) {
    if(m == 1) continue;
    m--;
    if(n%m == n/m) ans += m;
  }
