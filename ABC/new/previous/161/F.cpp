void find_divisor(ll N, vector<ll>& divisor) {
  for(int i=1; i<=sqrt(N); i++) {
    if(N % i == 0)
      divisor.push_back(i);
  }
  int n = divisor.size();
  for(int i=n-1; i>=0; i--) {
    if(N != (ll)pow(divisor[i], 2))
      divisor.push_back(N / divisor[i]);
  }
}
int main() {
  ll N;
  cin >> N;
  vector<ll> divisor1, divisor2;
  find_divisor(N, divisor1);
  find_divisor(N-1, divisor2);
  set<ll> se, ans;
  ans.insert(N);
  for(auto d: divisor1) se.insert(d);
  for(auto d: divisor2) if(d != 1) ans.insert(d);
  for(auto d: divisor1) {
    ll n = N;
    if(d == 1) continue;
    while(n % d == 0) n /= d;
    if(n % d == 1) ans.insert(d);
  }
  cout << ans.size() << endl;
  return 0;
}

